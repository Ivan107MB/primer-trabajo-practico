/*Angular es un framework de javascript de código abierto para el desarrollo de aplicaciones web.
 Es desarrollado y mantenido por google y se basa en typescript, 
un superconjunto tipado de javascript.
Es utilizado para crear aplicaciones de una sola página (single page applications - spa) con renderización del lado del cliente
(client-side rendering). A diferencia de las aplicaciones tradicionales, angular carga todos los recursos una sola vez, 
lo que evita recargar elementos como logos, fuentes y colores al navegar entre páginas. 
Esto hace que la experiencia sea más fluida, aunque implica un procesamiento adicional del lado del cliente*/
